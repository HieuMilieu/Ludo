package ApplicationLogic;

public class FieldOccupiedException extends Exception {

	public FieldOccupiedException() {
	}

	public FieldOccupiedException(String arg0) {
		super(arg0);
	}

	public FieldOccupiedException(Throwable arg0) {
		super(arg0);
	}

	public FieldOccupiedException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
